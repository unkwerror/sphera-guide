const T = [
  500, 800, 400, 400,
  350, 350, 350, 600,
  400, 300, 500, 600,
  400, 350, 350, 350,
  500, 800, 600,
  600, 500, 500, 500, 500, 500, 500,
  700,
];

export default function AsciiCatAnimated({ color = 'var(--color-text-tertiary)' }) {
  const [idx, setIdx] = useState(0);

  useEffect(() => {
    if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;
    let timer;
    const tick = () => {
      setIdx(prev => {
        const next = (prev + 1) % F.length;
        timer = setTimeout(tick, T[next] || 500);
        return next;
      });
    };
    timer = setTimeout(tick, T[0]);
    return () => clearTimeout(timer);
  }, []);

  return (
    <pre style={{
      fontFamily: 'var(--font-mono)',
      fontSize: '0.75rem',
      lineHeight: 1.15,
      color,
      userSelect: 'none',
      margin: 0,
      border: 'none',
      background: 'none',
      padding: 0,
      whiteSpace: 'pre',
    }}>
      {F[idx]}
    </pre>
  );
}
